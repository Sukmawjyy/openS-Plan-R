/**
 * claude-code-format.ts
 * Format adapter for Claude Code — generates a single CLAUDE.md file.
 * All rules are combined into one plain Markdown document with ## section headings.
 */

import type { SkillRule } from "../../core/skill-types.js";

export interface FormatOutput {
  filename: string;
  content: string;
}

/**
 * Formats skill rules into CLAUDE.md content.
 * Returns a single file that merges all rules under ## headings.
 */
export function formatRules(rules: SkillRule[]): FormatOutput[] {
  if (rules.length === 0) return [];

  const sections = rules.map((rule) => {
    const heading = `## ${rule.name}`;
    const meta = rule.description ? `\n<!-- ${rule.description} -->\n` : "";
    return `${heading}${meta}\n${rule.content.trim()}`;
  });

  const content = [
    "# Project Rules",
    "",
    "<!-- Generated by mcpman skill sync. Do not edit manually. -->",
    "",
    sections.join("\n\n"),
    "",
  ].join("\n");

  return [{ filename: "CLAUDE.md", content }];
}
